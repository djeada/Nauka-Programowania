# ZAD-03A — Dodawanie bitowe
#
# **Poziom:** ★☆☆
# **Tagi:** `bitwise`, `XOR`, `AND`
#
# ### Treść
#
# Wczytaj dwie liczby naturalne `a` i `b`. Oblicz `a + b` używając wyłącznie operatorów bitowych (i przesunięć).
#
# ### Wejście
#
# * 1. linia: `a`
# * 2. linia: `b`
#
# ### Wyjście
#
# Jedna liczba naturalna: `a + b`.
#
# ### Przykład
#
# **Wejście:**
#
# ```
# 2
# 3
# ```
#
# **Wyjście:**
#
# ```
# 5
