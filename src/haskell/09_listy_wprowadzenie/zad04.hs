--
-- w jednej linii, oddzielone pojedynczą spacją.
--
-- ### Wejście
--

-- * 1. linia: `N` (`N ≥ 1`)

-- * kolejne `N` linii: liczby całkowite

--
-- ### Wyjście
--
-- Jedna linia:
--

-- * `max min`

--
-- ### Przykład
--

-- ** Wejście:**

--
-- ```
-- 9
-- 4
-- -7
-- 8
-- 5
-- 6
-- -9
-- 10
-- 2
-- -8
-- ```
--

-- ** Wyjście:**

--
-- ```
-- 10 -9
-- ```
