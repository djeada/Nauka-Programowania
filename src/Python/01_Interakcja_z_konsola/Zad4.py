    """
    Wypisz wynik podstawowych operacji arytmetycznych dla dwoch liczb pobranych od uzytkownika.
    """

    x = int(input())
    y = int(input())

    print("suma pobranych liczb: ", x + y)
    print("roznica pobranych liczb: ", x - y)
    print("iloczyn pobranych liczb: ", x * y)
    print("iloraz pierwszej liczby przez druga liczbe: ", x / y)
    print("reszta z dzielenia pierwszej przez druga : ", x % y)
    print("pierwsza podniesiona do potegi drugiej : ", x ** y)
